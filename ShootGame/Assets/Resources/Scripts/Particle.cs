public class Particle
{
	//
	public float radius;
	public float angle;
	public float time; 

	public Particle()  
	{  
		this.radius = 0f;    
		this.angle = 0f;     
		this.time = 0f;       
	}

	public Particle(float _radius, float _angle, float _time)  
	{  
		radius = _radius;    
		angle = _angle;     
		time = _time;       
	}
}
